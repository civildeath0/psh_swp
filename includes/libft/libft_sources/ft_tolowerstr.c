#include "libft.h"

char	*ft_tolowerstr(char *str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		if ('A' <= str[i] && str[i] <= 'Z')
			str[i] += ('a' - 'A');
		++i;
	}
	return (str);
}
