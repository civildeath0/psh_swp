#include "libft.h"

size_t	ft_strlchr(char *str, char c)
{
	size_t	i;

	i = 0;
	if (str)
	{
		while (str[i])
		{
			if (str[i] == c)
				return (i);
			++i;
		}
	}
	return (0);
}
